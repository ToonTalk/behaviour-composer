package uk.ac.lkl.client;

import com.google.gwt.resources.client.ClientBundle;
import com.google.gwt.resources.client.DataResource;
import com.google.gwt.resources.client.ImageResource;

/**
 * This {@link ClientBundle} is used for all the button icons. Using an image
 * bundle allows all of these images to be packed into a single image, which
 * saves a lot of HTTP requests, drastically improving startup time.
 */
public interface Images extends ClientBundle {
    
    // Images common to BehaviourComposer and MoPiX
    
    @Source("uk/ac/lkl/client/images/closeTab.gif")
    DataResource closeTab();

    // For the RichTextEditor:

    @Source("uk/ac/lkl/client/images/bold.gif")
    ImageResource bold();

    @Source("uk/ac/lkl/client/images/createLink.gif")
    ImageResource createLink();

    @Source("uk/ac/lkl/client/images/hr.gif")
    ImageResource hr();

    @Source("uk/ac/lkl/client/images/indent.gif")
    ImageResource indent();

    @Source("uk/ac/lkl/client/images/insertImage.gif")
    ImageResource insertImage();

    @Source("uk/ac/lkl/client/images/italic.gif")
    ImageResource italic();

    @Source("uk/ac/lkl/client/images/justifyCenter.gif")
    ImageResource justifyCenter();

    @Source("uk/ac/lkl/client/images/justifyLeft.gif")
    ImageResource justifyLeft();

    @Source("uk/ac/lkl/client/images/justifyRight.gif")
    ImageResource justifyRight();

    @Source("uk/ac/lkl/client/images/ol.gif")
    ImageResource ol();

    @Source("uk/ac/lkl/client/images/outdent.gif")
    ImageResource outdent();

    @Source("uk/ac/lkl/client/images/removeFormat.gif")
    ImageResource removeFormat();

    @Source("uk/ac/lkl/client/images/removeLink.gif")
    ImageResource removeLink();

    @Source("uk/ac/lkl/client/images/strikeThrough.gif")
    ImageResource strikeThrough();

    @Source("uk/ac/lkl/client/images/subscript.gif")
    ImageResource subscript();

    @Source("uk/ac/lkl/client/images/superscript.gif")
    ImageResource superscript();

    @Source("uk/ac/lkl/client/images/ul.gif")
    ImageResource ul();

    @Source("uk/ac/lkl/client/images/underline.gif")
    ImageResource underline();

}
